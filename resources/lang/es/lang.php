										 	    <p align="justify"><span lang="ES">Sobre el homenajeado en Brasil, el Compositor Gilberto Gil:</span></p>
											    <p><span lang="ES"> </span></p>
										  	    <p align="justify"><span lang="ES"> Gilberto Gil es una de las más grandes personalidades de la música brasilera, reconocido en todo el mundo. Su carrera internacional ya le proporcionó un Grammy en la categoría Mejor Disco de World Music en 1998 y un Grammy Latino en 2003, entre otros premios importantes. </span></p>
											    <p><span lang="ES"> </span></p>
										 	    <p align="justify"><span lang="ES"> Gilberto Passos Gil Moreira nació en el Salvador y pasó la infancia en Ituaçu, en el interior de Bahía, donde comenzó a interesarse por la música oyendo a Orlando Silva y Luiz Gonzaga. A los 9 años se mudó para la capital de Bahía, Salvador y comenzó a aprender a tocar el acordeón. A los 18 años formó el conjunto “Os Desafinados”. A final de los años 1950, influenciado por Joao Gilberto pasó a tocar la guitarra. Durante la facultad de administración de empresas comenzó la música erudita contemporánea. En 1962 grabó su primer compacto (CD) solo y conoció a Caetano Veloso, Maria Bethania y Gal Costa. Al año siguiente, con Tom Zé integrando el grupo, hicieron el show “Nós, Por Exemplo”, en el Teatro Vila Velha, en Salvador. </span></p>
											    <p><span lang="ES"> </span></p>
										 	    <p align="justify"><span lang="ES"> Luego, Gil se mudó para Sao Paulo, donde trabajó en la empresa Gessy-Lever. En esa época conoció a Chico Buarque, Torquato Neto y Capinam. En 1965, cantó la música “Iemanjá”, en el 5° Festival de la Balanza, patrocinado por el Directorio Académico de la Facultad de Derecho de la Universidad Mackenzie que fue grabado por la RCA. </span></p>
											    <p><span lang="ES"> </span></p>
										  	    <p align="justify"><span lang="ES"> Gil se volvió conocido en el programa de televisión “O Fino da Bossa”, comandado por Elis Regina, donde presentó, entre otras, sus composiciones “Eu vim da Bahia” y “Louvação”. Con el éxito, dejó el empleo y firmó contrato con Philips que lanzó su primer LP, “Louvação” en 1967. Ya en Río de Janeiro, Gil participó de festivales de la Record y de la TV Rio y llegó a tener su propio programa en la TV Excélsior, el “Ensaio Geral”. </span></p>
											    <p><span lang="ES"> </span></p>
										 	    <p align="justify"><span lang="ES"> En el 3° Festival de la Record, en 1967, Gil presentó “Domingo no Parque” acompañado por los Mutantes y conquistó el segundo lugar. </span></p>
											    <p><span lang="ES"> </span></p>
											    <p align="justify"><span lang="ES">“Alegria, Alegria”, de Caetano Veloso quedó en cuarto lugar y formó junto con “Domingo no Parque” el embrión del movimiento tropicalista que mezclaba los elementos de la industria cultural y los materiales de la tradición brasilera. Diferente de Bossa Nova, el tropicalismo tenía una propuesta crítica, mostrando una preocupación con los problemas sociales del país. </span></p>
											    <p><span lang="ES"> </span></p>
												<p align="justify"><span lang="ES"> En 1968 fueron lanzados los LPs “Gilberto Gil” y “Tropicália o Panis et Circence” disco que contó, además de Caetano y Gil, con Los Mutantes, Torquato Neto, Capinam, Gal Costa, Tom Zé y Nara León. </span></p>
													    <p><span lang="ES"> </span></p>
												<p align="justify"><span lang="ES"> En 1969, Gil y Caetano Veloso fueron etiquetados de “subversivos” por el régimen militar y partieron para el exilio en Inglaterra. Retornaron al Brasil en 1972. Gil lanzó “Expreso 2222” y “Refazenda”. En el álbum “Realce”, de 1979, mostró su interés por el reggae y el pop. Son de esa época los LPs “Luz de la Luna”, “Um Banda Um”, “Extra”, “Raça Humana”, “Día Dorim, Noite Neón” y “O Eterno Deus Mu Dança”. </span></p>
													    <p><span lang="ES"> </span></p>
												<p align="justify"><span lang="ES Gil trabajó con Jimmy Cliff y en 1980 lanzó una versión en portugués del reggae “No Woman, No Cry” (“Não Chores Mais”) Suceso de Bob Marley. En 1993, con Caetano Veloso, lanzó “Tropicália 2” que incluía el rap en la zona “Haití”. </span></p>
													    <p><span lang="ES"> </span></p>
												<p align="justify"><span lang="ES"> Entre los discos “Quanta” y su versión al vivo, “Quanta Gente Veio Ver”, lanzó “O Sol de Oslo”, por el sello Pau Brasil. En el año 2000, en asociación con Milton Nascimento, ganó el disco “Gil e Milton”. </span></p>
													    <p><span lang="ES"> </span></p>
												<p align="justify"><span lang="ES"> Entre sus muchos éxitos, los más importantes fueron “Eu Preciso Aprender a Só Ser”, “Eu Só Quero um Xodó” (Dominguinhos/ Anastácia), “Punk da Periferia”, “Parabolicamará”, “Sítio do Pica Pau Amarelo”, “Soy Loco por Ti América” (con Capinam), “Realce”, “Toda Menina Baiana”, “Drão”, “Se Eu Quiser Falar com Deus” y muchas otras. </span></p>
													    <p><span lang="ES"> </span></p>
												<p align="justify"><span lang="ES"> De 1989 a 1992, Gil fue Regidor en la Cámara Municipal de Salvador por el Partido Verde. El 2 de enero del 2003 tomó posesión en el cargo de Ministro de Cultura, en el gobierno de Luiz Inácio Lula da Silva, al cual renunció en julio del 2008 para dedicarse a la carrera artística. </span></p>
